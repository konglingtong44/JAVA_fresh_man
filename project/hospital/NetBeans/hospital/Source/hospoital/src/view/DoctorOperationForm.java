    public void init(){
        DefaultComboBoxModel model = (DefaultComboBoxModel) jComboBoxDepartmentName.getModel();
        DoctorOperationDao doctorDepartments = new DoctorOperationDao();
        List<Doctor> doctorDepartmentList = doctorDepartments.getAllDoctor();
        
        for(int i = 0; i < doctorDepartmentList.size(); i++){
            Doctor doctor = doctorDepartmentList.get(i);
            model.addElement(doctor.getDepartmentName());
        }
        
        DefaultTableModel doctorDodel = (DefaultTableModel) jTableDoctorLIst.getModel();
        doctorDodel.setRowCount(0);
        
        DoctorOperationDao doctorOperationDao = new DoctorOperationDao();
        List<Doctor> doctors = doctorOperationDao.getAllDoctor();
        
        for (int i = 0; i < doctors.size(); i++){
            Doctor doctor = doctors.get(i);
            doctorDodel.addRow(new Object[]{doctor.getDoctorID(), doctor.getDoctorName(), doctor.getDoctorAge(), doctor.getDepartmentName()});
        }
    }

