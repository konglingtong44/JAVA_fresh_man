    private void jTableDoctorLIstMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableDoctorLIstMouseReleased
        // TODO add your handling code here:
        int row = jTableDoctorLIst.getSelectedRow();
        
        jTextFieldDoctorName.setText(jTableDoctorLIst.getValueAt(row, 1).toString());
        jTextFieldDocotrAge.setText(jTableDoctorLIst.getValueAt(row, 2).toString());
        String department = jTableDoctorLIst.getValueAt(row, 3).toString();
        DefaultComboBoxModel model = (DefaultComboBoxModel) jComboBoxDepartmentName.getModel();
      // JOptionPane.showMessageDialog(null, department);
        for(int i = 0; i < model.getSize(); i++){
            String doctor = (String)model.getElementAt(i);
            if(doctor.equals(department)){
                jComboBoxDepartmentName.setSelectedItem(doctor);
            }
        }
    }//GEN-LAST:event_jTableDoctorLIstMouseReleased


