    public void init(){
        DefaultTableModel model = (DefaultTableModel)BookType.getModel();
        model.setRowCount(0);
        TypeDao typeDao = new TypeDao();
        List<BookType> types = typeDao.getTypes();

        for(int i=0; i<types.size(); i++){
            BookType bookType = types.get(i);
            model.addRow(new Object[]{bookType.getBookNo(), bookType.getBookName()});
        }
    }
