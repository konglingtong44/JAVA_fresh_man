    private void jBUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBUpdateActionPerformed
        // TODO add your handling code here:
        String newID = BookTypeID.getText();
        String newName = BookTypeName.getText();
        BookType bookType = new BookType();
        bookType.setBookNo(Integer.parseInt(newID));
        bookType.setBookName(newName);
        
        UpdateTypeDao updateTypeDao = new UpdateTypeDao();
        int row = updateTypeDao.updateType(bookType);
        
        if(row == 1){
            JOptionPane.showMessageDialog(null, "成功");
            init();
        }else{
            JOptionPane.showMessageDialog(null, "失败");
        }
    }//GEN-LAST:event_jBUpdateActionPerformed


